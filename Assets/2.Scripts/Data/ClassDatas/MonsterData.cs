
public class MonsterData 
{
    public int monsterHP;
    public int monsterAtk;
    public int monsterDef;
    public float monsterSpeed;
    public float monsterJumpForce;

    public MonsterData(int _monsterHP, int _monsterAtk, int _monsterDef, float _monsterSpeed, float _monsterJumpForce)
    {
        this.monsterHP = _monsterHP;
        this.monsterAtk = _monsterAtk;
        this.monsterDef = _monsterDef;
        this.monsterSpeed = _monsterSpeed;
        this.monsterJumpForce = _monsterJumpForce;
    }
}
